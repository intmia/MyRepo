#include <iostream>
using namespace std;

int main() {
    int n;
    cin >> n;  // 输入花的株数
    
    int h[100005];  // 数组存储每株花的高度，n最大100000
    for (int i = 0; i < n; i++) {
        cin >> h[i];  // 依次输入每株花的高度
    }
    
    // 特殊情况：只有一株花，直接输出1
    if (n == 1) {
        cout << 1 << endl;
        return 0;
    }
    
    int ans = 1;   // 第一株花一定保留，所以答案至少为1
    int dir = 0;   // 记录当前"有效"的趋势方向
                   // 0: 还没有方向（初始状态）
                   // 1: 趋势向上（后一株比前一株高）
                   // -1: 趋势向下（后一株比前一株低）
    
    // 从第二株花开始扫描
    for (int i = 1; i < n; i++) {
        if (h[i] > h[i-1]) {        // 情况1：当前花比前一株高（上升）
            if (dir != 1) {         // 如果之前的方向不是上升
                ans++;              // 说明趋势发生了变化，保留当前花
                dir = 1;            // 更新方向为上升
            }
            // 如果之前方向已经是上升，说明还在同一波上升趋势中
            // 不保留当前花（因为波浪要求交替，连续上升中间的花要删掉）
        } 
        else if (h[i] < h[i-1]) {   // 情况2：当前花比前一株矮（下降）
            if (dir != -1) {        // 如果之前的方向不是下降
                ans++;              // 趋势发生变化，保留当前花
                dir = -1;           // 更新方向为下降
            }
            // 如果之前方向已经是下降，说明还在同一波下降趋势中
            // 不保留当前花
        }
        // 情况3：h[i] == h[i-1]（高度相等）
        // 什么都不做，不保留当前花，方向也不变
        // 因为相等不能形成上升或下降，直接跳过
    }
    
    cout << ans << endl;  // 输出最多能保留的花的数量
    return 0;
}